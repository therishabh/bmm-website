                            <h2>Cracking the Perfect Work from Home Make-up Look!</h2>
                            <img src="assets/images/blog-img-1.jpg" class="img-fluid">
                            <div class="blog-post-box-detail-body">
                                <div class="entry-content">

                                    <p>As we all have entered the ‘new normal’ in this fateful year 2020, work from home has become a norm for most of us. While initially this may have seemed like a blessing to many, soon the pattern had got us rather depressed and stuck. But fret not all you fabulous girls because ‘for all things love, magic and excitement, there is make-up’.</p>
                                    <p>Now, the million-dollar question is if it’s even really necessary to have make-up on when at home. Hell yes! And it’s not just to revive you to a peppy mood but also to help you look presentable for those video calls during the day with your colleagues. Trust me, good make-up hurt noone.</p>
                                    <p>Here, we have got a little guide to help all you beautiful women working from home to have a lovely start to your day, even if you are not stepping out. Showing up the right front with a well-defined look for the day indirectly speaks volumes about your professional approach and seriousness. Let’s see what all we need to do to get that perfect basic yet appealing and completely hassle-free work from home make-up look.</p>
                                    <p><b>Step 1</b>- Love Thy Skin- Do not ever forget to go for a good moisturizer, suited for your skin type, right after your bath. Nourishing your skin is a must. From Lakme Peach Milk Ultra Light Gel to L’Oreal Paris Hydra Fresh Anti-Ox Aqua Balm or the Plum Green Tea Mattifying Moisturizer, there is plenty to choose from.</p>
                                    <p><b>Step 2</b>- Brighten It Up- Having a base is the most important part of make-up but since we are at home, it is always better to let your skin breathe and go for lighter cosmetics like a CC/ BB cream instead of heavy foundation. Ponds White Beauty BB+ Cream is a very popular pick with many, while our vote goes to the super light Lakme 9 to 5 Naturale CC Cream with the goodness of aloe vera. It’s SPF 30 add-on makes it even better. You can also try the Colorbar Perfect Match BB Cream, which is slightly on the higher end but totally worth it.</p>
                                    <p><b>Step 3</b>- Give it Some Colour- Despite us wanting to keep our look fuss-free, some colour in those white bland cheeks is a must. So, I say we add a gorgeous matte blush in a soft tone to keep it subtle. Sugar Contour De Force Face Palette gives you two choices in pink with a bronzer to add, promising a dewy look and nothing too loud to stand out on camera. You can also check out Maybelline New York Fit Me Blush in an affordable range or the M.A.C. Matter Powder Blush for little more moolah but great finesse.</p>
                                    <p><b>Step 4</b>- Let the Eyes Talk- Our whole expression on the face is primarily ruled by our eyes and so, giving a little attention to highlighting our eyes is great for making the right impression on a call. A kajal or a mascara could do the job. While some like to use both, some prefer to go with just one at a time. For kajal, the clear winner is the Lakme Eyeconic Kajal but the Colorbar Just Smoky Eye Pencil can also be a nice pick. For mascara, choose from Maybelline New York Volum Express Hyper Curl Mascara or the Faces Canada Magneteyes Dramatic Volumizing one.</p>
                                    <p><b>Step 5</b>- Lips Don’t Lie- Last but not the least, bring in some nice hue to your pouty, perky lips to get just the right attention. Make sure to choose a toned-down colour that is not too jarring for a call with your close-up. Choose from shades of pinks, mauves or nudes to go with your outfit. From Colorbar to Sugar, M.A.C. to Faces, there are many to select from. But if you are not up for a proper lipstick at home, go for Maybelline New York Baby Lips lip balm to give you a natural and moisturizing feel with some tint.</p>
                                    <p>While with these steps, we have officially cracked the WFH make-up look that is easy to create and quick to apply. But putting the right clothes, giving a semi-formal look and a clean hairdo are also a must. You can also accessorize your look with simple earrings or neck chains to look work ready with a positive, refreshing vibe.</p>

