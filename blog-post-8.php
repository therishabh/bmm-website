                            <h2>Top 10 Beauty & Make-up Hacks Every Bride Can Rock for her D-day!</h2>
                            <img src="assets/images/blog-img-8.jpg" class="img-fluid">
                            <div class="blog-post-box-detail-body">
                                <div class="entry-content">
                                    <p>Weddings count for the most wonderful of days in anyone’s life and they are here to stay forever in one’s memories and of course, the pictures as a beautiful reminder for times to come. But you have to admit that they can be a handful when it comes to organizing everything perfectly and there’s no denying the level of stress a bride takes in. Now, while all the stress, anxiety and hard work feels totally worth it once you have those masterpiece photographs in front of you, it surely doesn’t hurt to know a few hacks to make life simpler. And amidst all other things to take care of, one thing a bride badly wants to make perfect is her beauty and make-up look for her special day as all eyes will be on her the entire time. </p>
                                    <p>With all wedding madness surrounding a bride and while she’s in her all new mushy phase, it’s important not to miss out on her beauty regime in prep for the big day. Let’s talk about a few easy-breezy beauty and make-up hacks that many beauty experts swear by and will be just about right for a bride to rock on/ before her D-day to make things simpler yet better still.</p>
                                    <p>1. Gel it Up- Being all rushed up, a bride should never forget giving attention to nick-nacks like her nails. It’s a great idea to go for gel nails, either yourself or from a professional, to avoid having your nails chipped before your special day. If doing at home, apply a smooth even coat and leave it to harden in the UV light and it’s done. Be ready to flaunt the perfect nails.</p>
                                    <p>2. No more Zits- What’s worse than waking up to a zit on any of your event days, all set to ruin those perfect moments and beautiful pictures. But before you even think about popping it up with your fingers, we have an amazing product recommendation for you. Use Q-tips by dipping them into a bowl of hot water and use it to remove the sticky stuff off your face gently. It’s super hygienic and prevents any spreading on your skin.</p>
                                    <p>3. Flyaways Go Away- Having those irritating flyaways right a few days before the day, use the toothbrush trick before you doze off for the night and voila, wake up with silky smooth hair the next morning. Apply a little hairspray to the toothbrush to comb your hair ends softly without overdoing it lest it causes split ends.</p>
                                    <p>4. Make Your Perfume Last- Often, even the best of perfumes tend to fade away on those heavy clothes and jewellery. You can make it last longer by simply applying a small amount of Vaseline on the inside of your wrists and neck area before applying a moderate quantity of your favourite perfume. </p>
                                    <p>5. Smile Bright with Teeth Shine- The last thing you want it to avoid smiling your heart out because of your teeth not looking good and bright. No need for a dentist appointment when you can just use a slice of lemon or strawberry, rubbing the yellow stains off from your teeth.</p>
                                    <p>6. Under Eyes Glow- All the dancing before your day while partying and all those sweet jittery sleepless nights can take a toll on your skin, especially under eye area. Here’s the perfect hack; just use frozen green tea bags to rejuvenate your skin before you go to bed, to look fresh as a daisy the next morning.</p>
                                    <p>7. Have that Lipstick Stay- Don’t keep that lipstick handy in your bestie’s clutch to redo every few hours rather apply a base coat of concealer or foundation after a lip scrub and before doing your lipstick to make sure it lasts all through and doesn’t patch up either. You may also try using a lip primer or powder with a double layer of lipstick to have it last way longer.</p>
                                    <p>8. Natural Look- Now, while this is the big day to look a bit over but nothing beats a classic, soft, and elegant look beside those heavy jewels and lehenga. Just take a good, easy mix of liquid highlighter in your moisturizer and apply it evenly on your face first before applying a layer of make-up of primer and foundation.</p>
                                    <p>9. Pay Attention to Nitty-Gritties- Taking care of small things goes a long way in making your look a hit or a miss, if you fail to do so. Pay good mind to your eyelashes, ensuring to curl them properly before applying your mascara. And for extra volume, apply mascara to both your upper and lower lashes and also press the brush once against your upper lash roots to give an amazing effect.</p>
                                    <p>10. Never Forget the Basics- Make sure to keep in mind the basic rules of make-up even on your most special day. Highlight either the eyes or the lips, don’t make it all too bright or too dull even. Also, avoid the white finishing powder to not look too pale rather use just a subtle blush shade for that dreamy dewy look.</p>
                                    <p>Just practice these simple yet amazing beauty hacks in preparation for your day and you will be all sorted with no unnecessary problems to worry about. </p>

